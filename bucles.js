let counter = 0

for(var i = counter+1; i < 6; i ++) {
    console.log(i)
}
console.log('Counting completed!');

for(var i = 5; i > counter; i --) {
    console.log(i)
}
console.log('Countdown finished!');

while (counter <= 5) {
    console.log(counter);
    counter++;
}
console.log('Counting completed!');

while(counter > 0) {
    console.log(counter);
    counter--;
}
console.log('Countdown finished!')

